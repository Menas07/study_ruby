# Concatenation

# operation plus

"Welcome" + "to" + "Menandro!" #-> Welcome to Menandro!

# operator shovel 

"Welcome " << "to" << "Menandro!" #-> Welcome to Menandro!

#concat method

"welcome ".concat("to").concat("Menandro!") #-> Welcome to Menandro!

# Substring

"hello"[0] #-> "h"
"hello"[0..1] #-> "he"
"hello"[0,4] #-> "hell"
"hello"[-1] #-> "o"
